#include<iostream>
using namespace std;


int main()
{
    int num1,num2;
    cout<<"Enter the value of num1: ";/* '<<' - this is called Insertion Operater*/
    cin>>num1; /*">>"- this is called Extraction operater*/

    cout<<"Enter the value of num2: ";/* '<<' - this is called Insertion Operater*/
    cin>>num2;/*">>"- this is called Extraction operater*/

    cout<<"The sum is :"<< num1+num2;

    return 0;
}
